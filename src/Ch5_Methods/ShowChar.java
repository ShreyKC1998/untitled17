package Ch5_Methods;

public class ShowChar {
    public static void main(String[] args) {

        System.out.println(showChar("New York", 2));
    }

    public static char showChar(String obj, int pos){

        char position = 0;
        for (int i = 0; i < obj.length(); i++) {
            pos = i;

        }
        return position;
    }

}


